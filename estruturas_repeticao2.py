# variável
texto = 'Olá, laço for.'

# for para iterar sobre cada caractere na variável
for item in texto:
    # imprime o caractere atual 
    print('Caractere: ' + item)

# for para o intervalo numérico de 1 a 10
for numero in range(1, 11):
    # imprime o número atual do intervalo
    print('Número do intervalo: ' + str(numero))